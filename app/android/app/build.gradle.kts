// Decode --dart-define-from-file values passed by Flutter
val dartEnv: Map<String, String> = if (project.hasProperty("dart-defines")) {
    project.property("dart-defines")
        .toString()
        .split(",")
        .associate { entry ->
            val pair = String(Base64.getDecoder().decode(entry)).split("=", limit = 2)
            pair[0] to pair[1]
        }
} else {
    emptyMap()
}

